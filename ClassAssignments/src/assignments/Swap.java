package assignments;

public class Swap {

	public static void main(String[] args) {

		int a = 1, b = 2, c = 0;
		System.out.println("a=" + a + "," + "b=" + b + "," + "c=" + c);
		c = a;
		System.out.println("a=" + a + "," + "b=" + b + "," + "c=" + c);
		a = b;
		System.out.println("a=" + a + "," + "b=" + b + "," + "c=" + c);
		b = c;
		System.out.println("a=" + a + "," + "b=" + b + "," + "c=" + c);

	}

}
